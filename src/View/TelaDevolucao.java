        String turma = TxfTurma.getText();
        String isbn = TxfIsbn.getText();
        
        if(nome.isEmpty() || turma.isEmpty() || isbn.isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
        }else{
        DAO dao = new DAO();
        dao.RegistrarDevolucao(nome, turma, isbn);}
        
        TxfNome.setText("");
        TxfTurma.setText("");
        TxfIsbn.setText("");

